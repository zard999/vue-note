<template>
  <div class="todo-header">
    <input
      type="text"
      v-model.trim="todoName"
      @keyup.enter="add"
      placeholder="请输入你的任务名称，按回车键确认"
    />
  </div>
</template>

<script>
export default {
  name: "TodoHeader",
  data() {
    return {
      todoName: "",
    };
  },

  methods: {
    add() {
      if (!this.todoName) return alert("输入不能为空");
      // add添加item
      this.$store.commit("add", this.todoName);
      this.todoName = "";
    },
  },
};
</script>

